int		is_upper(char c)
{
	return (c >= 'A' && c <= 'Z');
}

char	*ft_strlowcase(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		if (is_upper(str[i]))
			str[i] = str[i] + 32;
		i = i + 1;
	}
	return (str);
}
