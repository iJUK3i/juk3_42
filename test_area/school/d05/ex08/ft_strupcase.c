int		is_lower(char c)
{
	return (c >= 'a' && c <= 'z');
}

char	*ft_strupcase(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		if (is_lower(str[i]))
			str[i] = str[i] - 32;
		i = i + 1;
	}
	return (str);
}
