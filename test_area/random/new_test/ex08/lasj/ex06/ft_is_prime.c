int		ft_is_prime(int nb)
{
	int	i;
	int	flag;

	flag = 0;
	i = 2;
	while (i <= nb / 2)
	{
		i++;
		if (nb % i == 0)
		{
			flag = 1;
			return (0);
		}
	}
	return (1);
}
